#include <stdio.h>
/**
 * add - add two integer
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integer
 * @a: first int
 * @b: second int
 * Return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mutiply two integer
 * @a: first int
 * @b: second int
 * Return: mutiply
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - mod of two integer
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - add two integer
 * @a: first int
 * @b: second int
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
